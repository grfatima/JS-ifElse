let direction = prompt("Itiqamet daxil edin: (up,right,down,left)").toLowerCase();

if (direction == "up"){
    console.log("Yuxari get")

} else if (direction == "right"){
    console.log("Saga-a get")

} else if (direction == "down"){
    console.log("Asagi get")

}else if (direction == "left"){
    console.log("Sol-a get")

}else {
    console.log("Istiqameti yanlis daxil etdiniz.")
}

